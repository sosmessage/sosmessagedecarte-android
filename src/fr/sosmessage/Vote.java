package fr.sosmessage;

public class Vote {

	private int plus;
	private int minus;
	private int userVote;

	public void setPlus(int plus) {
		this.plus = plus;
	}

	public int getPlus() {
		return plus;
	}

	public void setMinus(int minus) {
		this.minus = minus;
	}

	public int getMinus() {
		return minus;
	}

	public void setUserVote(int userVote) {
		this.userVote = userVote;
	}

	public int getUserVote() {
		return userVote;
	}

}
